// Generate all grants
const allGrants = [
  ...federalGrants,
  ...foundationGrants,
  ...corporateGrants,
  ...states.flatMap(generateStateGrants),
];

async function main() {
  console.log(" Seeding database with comprehensive grant data...\n");

  // Clear existing grants
  await prisma.grant.deleteMany({});
  console.log("Cleared existing grants\n");

  console.log(`Creating ${allGrants.length} grants...\n`);

  // Insert all grants
  let created = 0;
  for (const grant of allGrants) {
    await prisma.grant.create({
      data: {
        ...grant,
        status: "discovered",
      },
    });
    created++;
    if (created % 25 === 0) {
      console.log(` Created ${created}/${allGrants.length} grants...`);
    }
  }

  console.log(`\n Successfully seeded ${created} grants!`);
  console.log(`   - ${federalGrants.length} Federal grants`);
  console.log(`   - ${foundationGrants.length} Foundation grants (private foundations)`);
  console.log(`   - ${corporateGrants.length} Corporate grants (corporate giving programs)`);
  console.log(`   - ${states.length * 4 + 6} State grants (across ${states.length} states)`);
}

main()
  .catch((e) => {
    console.error("Error seeding database:", e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
